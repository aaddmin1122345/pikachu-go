        雷·阿伦（Ray Allen），1975年7月20日出生于美国加利福尼亚州默塞德（Merced,California, USA），
        前美国职业篮球运动员，司职得分后卫，绰号"糖人"。
        1996年NBA选秀，阿伦于第1轮第5位被明尼苏达森林狼队选中，后被交易至密尔沃基雄鹿队，
        先后效力于密尔沃基雄鹿队、西雅图超音速队、波士顿凯尔特人队、迈阿密热火队。
